x<-c(10,20,30,40,50)
y<-c(15,25,35,30,45)


#scatter plot
plot(x,y,main="scatter plot",type="p",xlab="x",ylab="y")

#hist
hist(x,main="histogram",xlab="x",ylab="y")

#line chart
plot(x,main="line plot",type="l",xlab="x",ylab="y")

#pie chat
labels<-c("Lable1","Lable2","Lable3","Lable4","Lable5")
pie(x,labels=labels,main="pie chart")

#box plot
boxplot(x,col="lightblue",main="box plot",xlab="x",ylab="y")

